package eu.europa.esig.dss.enumerations;

/**
 * Rotation support
 *
 */
public enum VisualSignatureRotation {

	/**
	 * default, no rotate
	 */
	NONE,

	/**
	 * automatically rotate
	 */
	AUTOMATIC,

	/**
	 * rotate by 90
	 */
	ROTATE_90,

	/**
	 * rotate by 180
	 */
	ROTATE_180,

	/**
	 * rotate by 270
	 */
	ROTATE_270;

}